import fs from 'node:fs/promises';
import path from 'node:path';

const args = process.argv.slice(2);
const DEFAULT_INPUT_DIR = path.resolve(process.cwd(), 'test-results', 'ocr-backend-runs');
const DEFAULT_OUTPUT_PATH = path.resolve(process.cwd(), 'reports', 'ocr-performance.md');
const TRACKED_BACKENDS = ['tesseract', 'paddleonnx'];

const { inputDir, outputPath } = parseArgs(args);
const runs = await Promise.all(TRACKED_BACKENDS.map((backend) => loadBackendRun(inputDir, backend)));
const report = renderReport(runs, loadGitHubContext());

await fs.mkdir(path.dirname(outputPath), { recursive: true });
await fs.writeFile(outputPath, report);

console.log(`Saved OCR performance report: ${outputPath}`);

function parseArgs(commandArgs) {
    let inputDir = DEFAULT_INPUT_DIR;
    let outputPath = DEFAULT_OUTPUT_PATH;

    for (let index = 0; index < commandArgs.length; index += 1) {
        const argument = commandArgs[index];

        if (argument === '--input-dir') {
            inputDir = path.resolve(process.cwd(), readRequiredValue(commandArgs, index, '--input-dir'));
            index += 1;
            continue;
        }

        if (argument === '--output') {
            outputPath = path.resolve(process.cwd(), readRequiredValue(commandArgs, index, '--output'));
            index += 1;
            continue;
        }

        throw new Error(`Unknown argument: ${argument}`);
    }

    return { inputDir, outputPath };
}

function readRequiredValue(commandArgs, index, flagName) {
    const value = commandArgs[index + 1];

    if (!value || value.startsWith('--')) {
        throw new Error(`Missing value for ${flagName}`);
    }

    return value;
}

async function loadBackendRun(inputDir, backend) {
    const runDir = path.join(inputDir, backend);
    const summary = await readJson(path.join(runDir, 'ocr-accuracy-summary.json'));
    const metadata = await readJson(path.join(runDir, 'ocr-run-metadata.json'));
    const run = summary?.run ?? metadata ?? null;

    return {
        backend,
        label: backend === 'paddleonnx' ? 'PaddleOCR ONNX' : 'Tesseract',
        runDir,
        summary,
        run,
        status: normalizeStatus(summary, run),
        model: summary?.model ?? run?.model ?? 'n/a',
        paddleOnnxModelVariant: summary?.paddleOnnxModelVariant ?? run?.paddleOnnxModelVariant ?? null,
        totalCases: summary?.totalCases ?? null,
        exactMatches: summary?.exactMatches ?? null,
        exactMatchRate: summary?.exactMatchRate ?? null,
        averageCharacterAccuracy: summary?.averageCharacterAccuracy ?? null,
        cases: Array.isArray(summary?.cases) ? summary.cases : [],
    };
}

async function readJson(filePath) {
    try {
        const body = await fs.readFile(filePath, 'utf8');
        return JSON.parse(body);
    } catch {
        return null;
    }
}

function normalizeStatus(summary, run) {
    if (typeof run?.status === 'string' && run.status) {
        return run.status;
    }

    if (summary) {
        return 'passed';
    }

    return 'missing';
}

function loadGitHubContext() {
    const repository = process.env.GITHUB_REPOSITORY?.trim() || '';
    const serverUrl = process.env.GITHUB_SERVER_URL?.trim() || '';
    const sha = process.env.GITHUB_SHA?.trim() || '';
    const refName = process.env.GITHUB_REF_NAME?.trim() || '';
    const runId = process.env.GITHUB_RUN_ID?.trim() || '';
    const runNumber = process.env.GITHUB_RUN_NUMBER?.trim() || '';

    return {
        repository,
        sha,
        refName,
        runNumber,
        commitUrl: serverUrl && repository && sha ? `${serverUrl}/${repository}/commit/${sha}` : '',
        runUrl: serverUrl && repository && runId ? `${serverUrl}/${repository}/actions/runs/${runId}` : '',
    };
}

function renderReport(runs, gitHubContext) {
    const generatedAt = new Date().toISOString();
    const tesseractRun = runs.find((run) => run.backend === 'tesseract') ?? null;
    const paddleRun = runs.find((run) => run.backend === 'paddleonnx') ?? null;
    const comparison = buildComparison(tesseractRun, paddleRun);
    const caseRows = buildCaseRows(tesseractRun, paddleRun);
    const failureRuns = runs.filter((run) => run.status !== 'passed');
    const lines = [
        '<!-- Generated by .github/workflows/ocr-performance.yml. Do not edit manually. -->',
        '',
        '# OCR Performance Report',
        '',
        `Generated: ${generatedAt}`,
    ];

    if (gitHubContext.repository) {
        lines.push(`Repository: \`${gitHubContext.repository}\``);
    }

    if (gitHubContext.refName) {
        lines.push(`Branch: \`${gitHubContext.refName}\``);
    }

    if (gitHubContext.sha) {
        const shortSha = gitHubContext.sha.slice(0, 7);
        lines.push(gitHubContext.commitUrl
            ? `Commit: [\`${shortSha}\`](${gitHubContext.commitUrl})`
            : `Commit: \`${shortSha}\``);
    }

    if (gitHubContext.runNumber) {
        lines.push(gitHubContext.runUrl
            ? `Workflow run: [#${gitHubContext.runNumber}](${gitHubContext.runUrl})`
            : `Workflow run: #${gitHubContext.runNumber}`);
    }

    lines.push(
        '',
        '## Summary',
        '',
        '| Backend | Status | Model | Variant | Exact Matches | Exact Match Rate | Avg Character Accuracy | Build Time | Test Time | Total Time |',
        '| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |',
    );

    for (const run of runs) {
        lines.push([
            run.label,
            formatStatus(run.status),
            escapeTableCell(run.model),
            escapeTableCell(run.paddleOnnxModelVariant ?? 'n/a'),
            formatExactMatches(run.exactMatches, run.totalCases),
            formatPercent(run.exactMatchRate),
            formatPercent(run.averageCharacterAccuracy),
            formatDuration(run.run?.buildDurationMs ?? null),
            formatDuration(run.run?.testDurationMs ?? null),
            formatDuration(run.run?.totalDurationMs ?? null),
        ].join(' | ').replace(/^/, '| ').concat(' |'));
    }

    lines.push(
        '',
        '## Comparison',
        '',
    );

    if (comparison) {
        lines.push(
            '| Metric | Tesseract | PaddleOCR ONNX | Delta |',
            '| --- | --- | --- | --- |',
            `| Exact matches | ${formatExactMatches(tesseractRun?.exactMatches ?? null, tesseractRun?.totalCases ?? null)} | ${formatExactMatches(paddleRun?.exactMatches ?? null, paddleRun?.totalCases ?? null)} | ${formatCountDelta((paddleRun?.exactMatches ?? null), (tesseractRun?.exactMatches ?? null))} |`,
            `| Exact match rate | ${formatPercent(tesseractRun?.exactMatchRate ?? null)} | ${formatPercent(paddleRun?.exactMatchRate ?? null)} | ${formatPercentPointDelta((paddleRun?.exactMatchRate ?? null), (tesseractRun?.exactMatchRate ?? null))} |`,
            `| Avg character accuracy | ${formatPercent(tesseractRun?.averageCharacterAccuracy ?? null)} | ${formatPercent(paddleRun?.averageCharacterAccuracy ?? null)} | ${formatPercentPointDelta((paddleRun?.averageCharacterAccuracy ?? null), (tesseractRun?.averageCharacterAccuracy ?? null))} |`,
            `| Total time | ${formatDuration(tesseractRun?.run?.totalDurationMs ?? null)} | ${formatDuration(paddleRun?.run?.totalDurationMs ?? null)} | ${formatDurationDelta((paddleRun?.run?.totalDurationMs ?? null), (tesseractRun?.run?.totalDurationMs ?? null))} |`,
        );
    } else {
        lines.push('Full comparison is unavailable because one or both backend summaries are missing.');
    }

    lines.push(
        '',
        '## Case Accuracy',
        '',
    );

    if (caseRows.length > 0) {
        lines.push(
            '| Case | Tesseract | PaddleOCR ONNX | Delta | Better |',
            '| --- | --- | --- | --- | --- |',
        );

        for (const row of caseRows) {
            lines.push(`| ${escapeTableCell(row.name)} | ${formatPercent(row.tesseractAccuracy)} | ${formatPercent(row.paddleAccuracy)} | ${formatPercentPointDelta(row.paddleAccuracy, row.tesseractAccuracy)} | ${row.better} |`);
        }
    } else {
        lines.push('No per-case OCR data was available.');
    }

    if (failureRuns.length > 0) {
        lines.push(
            '',
            '## Failures',
            '',
            '| Backend | Status | Build Exit | Test Exit | Overall Exit |',
            '| --- | --- | --- | --- | --- |',
        );

        for (const run of failureRuns) {
            lines.push(`| ${run.label} | ${formatStatus(run.status)} | ${formatNumber(run.run?.buildExitCode ?? null)} | ${formatNumber(run.run?.testExitCode ?? null)} | ${formatNumber(run.run?.exitCode ?? null)} |`);
        }
    }

    lines.push(
        '',
        '## Notes',
        '',
        '- This report is generated from `npm run test:e2e:tesseract` and `npm run test:e2e:paddleonnx` results.',
        '- Accuracy metrics come from `test-results/ocr-accuracy-summary.json` for each backend run.',
        '- Timing metrics are wall-clock build and Playwright durations recorded by `run-playwright-e2e.mjs`.',
        '',
    );

    return lines.join('\n');
}

function buildComparison(tesseractRun, paddleRun) {
    if (!tesseractRun?.summary || !paddleRun?.summary) {
        return null;
    }

    return {
        tesseractRun,
        paddleRun,
    };
}

function buildCaseRows(tesseractRun, paddleRun) {
    const tesseractCases = mapCasesByName(tesseractRun?.cases ?? []);
    const paddleCases = mapCasesByName(paddleRun?.cases ?? []);
    const caseNames = Array.from(new Set([
        ...tesseractCases.keys(),
        ...paddleCases.keys(),
    ]));

    const rows = caseNames.map((name) => {
        const tesseractCase = tesseractCases.get(name) ?? null;
        const paddleCase = paddleCases.get(name) ?? null;
        const tesseractAccuracy = typeof tesseractCase?.characterAccuracy === 'number'
            ? tesseractCase.characterAccuracy
            : null;
        const paddleAccuracy = typeof paddleCase?.characterAccuracy === 'number'
            ? paddleCase.characterAccuracy
            : null;

        return {
            caseIndex: resolveCaseIndex(tesseractCase, paddleCase),
            name,
            tesseractAccuracy,
            paddleAccuracy,
            better: resolveBetterBackend(tesseractAccuracy, paddleAccuracy),
        };
    });

    return rows.sort((left, right) => {
        if (left.caseIndex !== right.caseIndex) {
            return left.caseIndex - right.caseIndex;
        }

        return left.name.localeCompare(right.name);
    });
}

function mapCasesByName(cases) {
    const entries = new Map();

    for (const result of cases) {
        if (result && typeof result.name === 'string' && result.name) {
            entries.set(result.name, result);
        }
    }

    return entries;
}

function resolveCaseIndex(left, right) {
    if (Number.isInteger(left?.caseIndex)) {
        return left.caseIndex;
    }

    if (Number.isInteger(right?.caseIndex)) {
        return right.caseIndex;
    }

    return Number.MAX_SAFE_INTEGER;
}

function resolveBetterBackend(tesseractAccuracy, paddleAccuracy) {
    if (typeof tesseractAccuracy !== 'number' || typeof paddleAccuracy !== 'number') {
        return 'n/a';
    }

    if (paddleAccuracy > tesseractAccuracy) {
        return 'PaddleOCR ONNX';
    }

    if (paddleAccuracy < tesseractAccuracy) {
        return 'Tesseract';
    }

    return 'Tie';
}

function formatStatus(status) {
    switch (status) {
        case 'build_failed':
            return 'Build failed';
        case 'failed':
            return 'Failed';
        case 'missing':
            return 'Missing';
        default:
            return 'Passed';
    }
}

function formatExactMatches(exactMatches, totalCases) {
    if (!Number.isFinite(exactMatches) || !Number.isFinite(totalCases)) {
        return 'n/a';
    }

    return `${exactMatches}/${totalCases}`;
}

function formatPercent(value) {
    if (!Number.isFinite(value)) {
        return 'n/a';
    }

    return `${(value * 100).toFixed(1)}%`;
}

function formatPercentPointDelta(left, right) {
    if (!Number.isFinite(left) || !Number.isFinite(right)) {
        return 'n/a';
    }

    const delta = (left - right) * 100;
    const sign = delta > 0 ? '+' : '';
    return `${sign}${delta.toFixed(1)} pp`;
}

function formatCountDelta(left, right) {
    if (!Number.isFinite(left) || !Number.isFinite(right)) {
        return 'n/a';
    }

    const delta = left - right;
    const sign = delta > 0 ? '+' : '';
    return `${sign}${delta}`;
}

function formatDuration(value) {
    if (!Number.isFinite(value)) {
        return 'n/a';
    }

    const roundedMs = Math.max(0, Math.round(value));
    const totalSeconds = Math.round(roundedMs / 1000);
    const minutes = Math.floor(totalSeconds / 60);
    const seconds = totalSeconds % 60;

    if (minutes === 0) {
        return `${seconds}s`;
    }

    return `${minutes}m ${seconds}s`;
}

function formatDurationDelta(left, right) {
    if (!Number.isFinite(left) || !Number.isFinite(right)) {
        return 'n/a';
    }

    const delta = Math.round(left - right);
    const sign = delta > 0 ? '+' : delta < 0 ? '-' : '';
    return delta === 0 ? '0s' : `${sign}${formatDuration(Math.abs(delta))}`;
}

function formatNumber(value) {
    return Number.isFinite(value) ? String(value) : 'n/a';
}

function escapeTableCell(value) {
    return String(value).replace(/\|/g, '\\|').replace(/\r?\n/g, ' ');
}
